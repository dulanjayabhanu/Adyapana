    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String username = jTextField1.getText();
        String password = String.valueOf(jPasswordField1.getPassword());
        
        if(username.isEmpty()){
            JOptionPane.showMessageDialog(this, "Please enter your username", "Warning", JOptionPane.WARNING_MESSAGE);
        }else if(username.length() > 20 | username.length() < 4){
            JOptionPane.showMessageDialog(this, "Username must have 4-20 characters", "Warning", JOptionPane.WARNING_MESSAGE);
        }else if(password.isEmpty()){
            JOptionPane.showMessageDialog(this, "Please enter your password", "Warning", JOptionPane.WARNING_MESSAGE);
        }else if(password.length() > 16 | password.length() < 8){
            JOptionPane.showMessageDialog(this, "Password must have 8-16 characters", "Warning", JOptionPane.WARNING_MESSAGE);
        }else{
            try{
                ResultSet resultset = MySQL.execute("SELECT * FROM `admin` WHERE `username`='" + username + "' AND `password`='" + password + "'");
                
                if(resultset.next()){
                    adminMap.put("firstName", resultset.getString("first_name"));
                    adminMap.put("lastName", resultset.getString("last_name"));
                    adminMap.put("username", resultset.getString("username"));
                    
                    AdminPanel adminPanel = new AdminPanel(adminMap);
                    adminPanel.setVisible(true);
                    this.dispose();
                }else{
                    JOptionPane.showMessageDialog(this, "Invalied username or passowrd", "Warning", JOptionPane.WARNING_MESSAGE);
                }
            }catch(Exception e){
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

